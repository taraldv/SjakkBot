#include <iostream>

#include "bitboard.h"
#include "position.h"
#include "search.h"
#include "thread.h"
#include "tt.h"
#include "uci.h"
#include "syzygy/tbprobe.h"

namespace PSQT {
  void init();
}

int main() {
  UCI::init(Options);
  PSQT::init();
  Bitboards::init();
  Position::init();
  Bitbases::init();
  Search::init();
  Pawns::init();
  Threads.set(Options["Threads"]);
  Search::clear(); // After threads are up

  //simulate cmd inputs
  char* testFen = (char*)"rnbqkbnr/pp1ppppp/8/2p5/4P3/5N2/PPPP1PPP/RNBQKB1R b KQkq - 1 2";
  char* fen = (char*)"fen";
  char* position = (char*)"position";
  char* argv[3];
  argv[0] = position;
  argv[1] = fen;
  argv[2] = testFen;

  UCI::loop(3,argv);

  Threads.set(0);
  return 0;
}
